const add = (x, y) => x + y;
console.log(`Addition: ${add(5, 9)}`);

const multiply = (x, y) => x * y;
console.log(`Multiplication: ${multiply(3, 4)}`);

const divide = (x, y) => x / y;
console.log(`Division: ${divide(3, 5)}`);

const exponent = (base, power) => base ** power;
console.log(`Exponent: ${exponent(2, 5)}`);
